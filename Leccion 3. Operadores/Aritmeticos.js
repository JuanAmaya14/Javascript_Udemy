let a = 3,
  b = 2;

//Suma

let x = a + b;

console.log(x);

//Resta
x = a - b;

console.log(x);

//Multiplicacion
x = a * b;

console.log(x);

//Exponenciacion
x = a ** b;

console.log(x);

//Divicion
x = a / b;

console.log(x);

//Modulo(residuo de divicion)
x = a % b;

console.log(x);

//pre-incremento

x = ++a;
console.log(a);
console.log(x);

//post-incremento

x = b++;
console.log(b);
console.log(x);

//pre-decremento

x = --a;
console.log(a);
console.log(x);

//post-decremento

x = b--;
console.log(b);
console.log(x);

x = a += 3;
console.log(x);

x = a -= 4;
console.log(x);
